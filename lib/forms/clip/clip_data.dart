class ClipData {
  double afp;
  String cps;
  String tumourNumber;
  String tumourExtent;
  String pvt;
  String result;

  ClipData({
    double afp,
    String cps,
    String tumourNumber,
    String tumourExtent,
    String pvt,
    String result,
  }) {
    this.afp = afp;
    this.cps = cps;
    this.tumourNumber = tumourNumber;
    this.tumourExtent = tumourExtent;
    this.pvt = pvt;
    this.result = result;
  }
}
