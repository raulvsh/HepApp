class OkudaData {
  double bilirubin;
  double albumin;
  String ascites;
  String result;
  String tumourExtent;


  OkudaData({double bilirubin,
    double albumin,
    String ascites,
    String tumourExtent,
    String result}) {
    this.bilirubin = bilirubin;
    this.albumin = albumin;
    this.ascites = ascites;
    this.tumourExtent = tumourExtent;
    this.result = result;
  }
}
